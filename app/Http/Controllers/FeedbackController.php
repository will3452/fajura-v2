    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $data = $request->validate([
            'stars'=>'required',
            'star_validator'=>'required',
            'service_validator'=>'required',
            'service_id'=>'required',
            'message'=>''
        ]);

        if(\Hash::check($data['stars'], $data['star_validator']) && \Hash::check($data['service_id'], $data['service_validator'])){
            unset($data['service_validator']);
            unset($data['star_validator']);
            toast('Feedback submitted!');
            auth()->user()->feedbacks()->create($data);
            return back();
        }else {
            abort(401);
        }

        

    }

